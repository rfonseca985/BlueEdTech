# Dada a lista L = [5, 7, 2, 9, 4, 1, 3], escreva um programa que imprima as seguintes informações:
# tamanho da lista.
# maior valor da lista.
# menor valor da lista.
# soma de todos os elementos da lista.
# lista em ordem crescente.
